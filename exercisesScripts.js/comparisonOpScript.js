console.log("********** 7. COMPARISON OPERATOR **********");

// Boolean value is either true or false. Any comparison return a boolean either true or false.
// Use all the following comparison operators to compare the following values: >, < >=, <=, !=, !==,===.
// Which are true or which are false ?

var value1 = 4;
var value2 = 3;
// a. 4 > 3
if (value1 > value2) {
    console.log("TRUE.");
} else {
    console.log("FALSE.");
}
// b. 4 >= 3
if (value1 >= value2) {
    console.log("TRUE.");
} else {
    console.log("FALSE.");
}
// c. 4 < 3
if (value1 < value2) {
    console.log("TRUE.");
} else {
    console.log("FALSE.");
}
// d. 4 <=3
if (value1 <= value2) {
    console.log("TRUE.");
} else {
    console.log("FALSE.");
}
// e. 4 == 4
if (value1 == value1) {
    console.log("TRUE.");
} else {
    console.log("FALSE.");
}
// f. 4 === 4
if (value1 === value1) {
    console.log("TRUE.");
} else {
    console.log("FALSE.");
}
// g. 4 != 4
if (value1 != value1) {
    console.log("TRUE.");
} else {
    console.log("FALSE.");
}
// h. 4!== 4
if (value1 !== value1) {
    console.log("TRUE.");
} else {
    console.log("FALSE.");
}
// i. 4!=’4’
if (4 != "4") {
    console.log("TRUE.");
} else {
    console.log("FALSE.");
}
// j. 4==’4’
if (4 == "4") {
    console.log("TRUE.");
} else {
    console.log("FALSE.");
}
// k. 4 ===’4’
if (4 === "4") {
    console.log("TRUE.");
} else {
    console.log("FALSE.");
}

